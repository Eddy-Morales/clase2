public class Series {
    String titulo;
    String genero;
    String director;
    int anio_estreno;
    int duracion;

    public Series(String titulo, String genero, String director) {
        this.titulo = titulo;
        this.genero = genero;
        this.director = director;
        this.anio_estreno = anio_estreno;
        this.duracion = duracion;

    }
}